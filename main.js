function applyLanguage(lang) {
  const dict = STRINGS[lang] || STRINGS.en;
  document.documentElement.lang = lang === "ur" ? "ur" : "en";
  document.body.dir = lang === "ur" ? "rtl" : "ltr";

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (!key || !dict[key]) return;
    let text = dict[key];
    if (key === "footerNote") {
      const y = String(new Date().getFullYear());
      text = text.replace("{year}", y);
    }
    el.textContent = text;
  });

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    const isActive = btn.getAttribute("data-lang") === lang;
    btn.classList.toggle("is-active", isActive);
    btn.setAttribute("aria-pressed", isActive ? "true" : "false");
  });

  try {
    localStorage.setItem("dwt-lang", lang);
  } catch {
    /* ignore */
  }

  requestAnimationFrame(() => relayoutCarousels());
}

function initCarousels() {
  document.querySelectorAll("[data-carousel]").forEach((root) => {
    const viewport = root.querySelector(".carousel-viewport");
    const track = root.querySelector(".carousel-track");
    const slides = root.querySelectorAll(".carousel-slide");
    const prev = root.querySelector(".carousel-prev");
    const next = root.querySelector(".carousel-next");
    const dots = root.querySelectorAll(".carousel-dot");
    if (!viewport || !track || !slides.length) return;

    const total = slides.length;
    let index = 0;
    let autoplayId = null;
    let hasUserInteracted = false;
    const prefersReduced =
      typeof window.matchMedia === "function" &&
      window.matchMedia("(prefers-reduced-motion: reduce)").matches;

    function layout() {
      const w = viewport.offsetWidth;
      if (w <= 0) return;
      track.style.width = `${w * total}px`;
      slides.forEach((slide) => {
        slide.style.flex = "0 0 auto";
        slide.style.width = `${w}px`;
      });
      applyTransform();
    }

    function applyTransform() {
      const w = viewport.offsetWidth;
      track.style.transform = `translateX(-${index * w}px)`;
    }

    function syncState() {
      slides.forEach((s, i) => s.setAttribute("aria-hidden", i === index ? "false" : "true"));
      dots.forEach((d, i) => {
        const on = i === index;
        d.classList.toggle("is-active", on);
        d.setAttribute("aria-selected", on ? "true" : "false");
      });
    }

    function go(delta) {
      index = (index + delta + total) % total;
      applyTransform();
      syncState();
    }

    function goTo(i) {
      index = i;
      applyTransform();
      syncState();
    }

    track.style.display = "flex";
    track.style.transition = prefersReduced ? "none" : "transform 0.4s ease";

    function stopAutoplay() {
      hasUserInteracted = true;
      if (autoplayId !== null) {
        window.clearInterval(autoplayId);
        autoplayId = null;
      }
    }

    function startAutoplay() {
      if (prefersReduced || hasUserInteracted || total < 2) return;
      if (autoplayId !== null) window.clearInterval(autoplayId);
      autoplayId = window.setInterval(() => go(1), 4000);
    }

    layout();
    syncState();
    startAutoplay();

    prev?.addEventListener("click", () => {
      stopAutoplay();
      go(-1);
    });
    next?.addEventListener("click", () => {
      stopAutoplay();
      go(1);
    });
    dots.forEach((dot, i) => {
      dot.addEventListener("click", () => {
        stopAutoplay();
        goTo(i);
      });
    });

    root.addEventListener("pointerdown", stopAutoplay, { passive: true });
    root.addEventListener("touchstart", stopAutoplay, { passive: true });
    root.addEventListener("mouseenter", stopAutoplay);
    root.addEventListener("focusin", stopAutoplay);
    root.addEventListener("keydown", stopAutoplay);

    if (typeof ResizeObserver !== "undefined") {
      const ro = new ResizeObserver(() => layout());
      ro.observe(viewport);
    } else {
      window.addEventListener("resize", layout);
    }

    root._dwtCarouselLayout = layout;
  });
}

function relayoutCarousels() {
  document.querySelectorAll("[data-carousel]").forEach((root) => {
    if (typeof root._dwtCarouselLayout === "function") {
      root._dwtCarouselLayout();
    }
  });
}

document.querySelectorAll(".lang-btn").forEach((btn) => {
  btn.addEventListener("click", () => {
    const lang = btn.getAttribute("data-lang");
    if (lang === "en" || lang === "ur") applyLanguage(lang);
  });
});

initCarousels();

let initial = "en";
try {
  const saved = localStorage.getItem("dwt-lang");
  if (saved === "en" || saved === "ur") initial = saved;
} catch {
  /* ignore */
}
applyLanguage(initial);
