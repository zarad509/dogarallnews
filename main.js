function applyLanguage(lang) {
  const dict = STRINGS[lang] || STRINGS.en;
  document.documentElement.lang = lang === "ur" ? "ur" : "en";
  document.body.dir = lang === "ur" ? "rtl" : "ltr";

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (!key || !dict[key]) return;
    let text = dict[key];
    if (key === "footerNote") {
      const y = String(new Date().getFullYear());
      text = text.replace("{year}", y);
    }
    el.textContent = text;
  });

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    const isActive = btn.getAttribute("data-lang") === lang;
    btn.classList.toggle("is-active", isActive);
    btn.setAttribute("aria-pressed", isActive ? "true" : "false");
  });

  try {
    localStorage.setItem("dwt-lang", lang);
  } catch {
    /* ignore */
  }
}

function initTabs() {
  const root = document.querySelector("[data-tabs]");
  if (!root) return;

  const tabs = Array.from(root.querySelectorAll('[role="tab"]'));
  const panels = Array.from(root.querySelectorAll('[role="tabpanel"]'));

  function showTab(index) {
    tabs.forEach((tab, i) => {
      const selected = i === index;
      tab.setAttribute("aria-selected", selected ? "true" : "false");
      tab.tabIndex = selected ? 0 : -1;
    });
    panels.forEach((panel, i) => {
      const active = i === index;
      panel.toggleAttribute("hidden", !active);
      panel.classList.toggle("is-active", active);
    });
  }

  tabs.forEach((tab, index) => {
    tab.addEventListener("click", () => showTab(index));
    tab.addEventListener("keydown", (e) => {
      const rtl = document.body.getAttribute("dir") === "rtl";
      const forward = rtl ? "ArrowLeft" : "ArrowRight";
      const back = rtl ? "ArrowRight" : "ArrowLeft";
      let next = index;
      if (e.key === forward || e.key === "ArrowDown") {
        e.preventDefault();
        next = (index + 1) % tabs.length;
      } else if (e.key === back || e.key === "ArrowUp") {
        e.preventDefault();
        next = (index - 1 + tabs.length) % tabs.length;
      } else if (e.key === "Home") {
        e.preventDefault();
        next = 0;
      } else if (e.key === "End") {
        e.preventDefault();
        next = tabs.length - 1;
      } else {
        return;
      }
      tabs[next].focus();
      showTab(next);
    });
  });
}

document.querySelectorAll(".lang-btn").forEach((btn) => {
  btn.addEventListener("click", () => {
    const lang = btn.getAttribute("data-lang");
    if (lang === "en" || lang === "ur") applyLanguage(lang);
  });
});

